    private void table_SPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_SPMouseClicked
         try{
             int row = table_SP.getSelectedRow();
             if(row >= 0){
                 String id = (String) table_SP.getValueAt(row, 0);
                 LapTopDao dao = new LapTopDao();
                 LapTop lt = dao.findByID(id);
                 
                 if(lt != null){
                     txt_MaSP.setText(lt.getMaSP());
                     txt_TenSP.setText(lt.getTenSP());
                     txt_Hang.setText(lt.getHang());
                     txt_CPU.setText(lt.getCPU());
                     txt_SoLuong.setText(String.format("%d", lt.getSoLuong()) );
                     txt_GiaGoc.setText(String.format("%.1f", lt.getGiaGoc()));
                     btn_Moi.setSelected(lt.getTinhTrang()==1?true:false);
                     if(lt.getAnh ()!= null){
                         Image img = ImageHelper.createImageFromByteArray(lt.getAnh(), "jpg");
                         lbl_Image.setIcon(new ImageIcon(img));
                         personalImage = lt.getAnh();
                     }else{
                         personalImage = lt.getAnh();
                         ImageIcon icon = new ImageIcon(getClass().getResource("/com/mycompany/quanlybanhangonl/icons/open-file-icon-48.png"));
                         lbl_Image.setIcon(icon);
                     }
                 }
             }
         }catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
         }
        
        
    }//GEN-LAST:event_table_SPMouseClicked

