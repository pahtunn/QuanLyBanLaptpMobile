    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        //Nút saved
        StringBuilder sb = new StringBuilder();
        DataValidator.ValidateEmpty(txt_MaSP,sb,"Mã sản phẩm không được để trống");
        DataValidator.ValidateEmpty(txt_TenSP,sb,"Tên sản phẩm không được để trống");
        DataValidator.ValidateEmpty(txt_Hang,sb,"Hãng sản phẩm không được để trống");
        DataValidator.ValidateEmpty(txt_SoLuong,sb,"Số lượng sản phẩm không được để trống");
        DataValidator.ValidateEmpty(txt_GiaGoc,sb,"Giá gốc sản phẩm không được để trống");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try{
            LapTop lt = new LapTop();
            lt.setMaSP(txt_MaSP.getText());
            lt.setTenSP(txt_TenSP.getText());
            lt.setHang(txt_Hang.getText());
            lt.setCPU(txt_CPU.getText());
            lt.setGiaGoc(Float.parseFloat(txt_GiaGoc.getText()));
            lt.setSoLuong(Integer.parseInt(txt_SoLuong.getText()));
            lt.setMaSP(txt_MaSP.getText());
            lt.setTinhTrang(btn_Moi.isSelected()?1: 0);
            lt.setAnh(personalImage);
            
            LapTopDao dao = new LapTopDao();
            if(dao.insert(lt)){
                MessageDialogHelper.showMessageDialog(parentForm, "Thông tin đã được lưu", "Thông báo");
                loadDataToTable();
            }else{
              MessageDialogHelper.showMessageDialog(parentForm, "Thông tin không được lưu do lỗi!", "Thông báo");
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,
                    e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_SaveActionPerformed

