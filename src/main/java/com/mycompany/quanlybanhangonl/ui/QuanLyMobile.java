    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // nút DELETE
        StringBuilder sb = new StringBuilder();
        DataValidator.ValidateEmpty(txt_MaSP,sb,"Mã sản phẩm không được để trống");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa sản phẩm không?",
                "Hỏi")== JOptionPane.NO_OPTION){
            return;
        }

        try{
            Mobile mb = new Mobile();
            mb.setMaSP(txt_MaSP.getText());
            MobileDao dao = new MobileDao();
            if(dao.delete(mb)){
                MessageDialogHelper.showMessageDialog(parentForm, "Thông tin đã được xóa", "Thông báo");
                loadDataToTable();
            }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Thông tin chưa được xóa do lỗi!", "Thông báo");
                btnNewActionPerformed(evt);
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,
                    e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

