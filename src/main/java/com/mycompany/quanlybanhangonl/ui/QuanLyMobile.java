    private void btn_AnhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AnhActionPerformed
        JFileChooser chooser = new  JFileChooser();
        chooser.setFileFilter(new FileFilter(){
                   
            @Override
            public boolean accept(File f){
                if(f.isDirectory()){
                    return true;
                }else{
                    return f.getName().toLowerCase().endsWith(".jpg");
                }
            }
            
            @Override
            public String getDescription(){
                return "Image File (*.jpg)";
            }
        });
        
        if(chooser.showOpenDialog(parentForm)== JFileChooser.CANCEL_OPTION){
            return;
        }
        
        File file = chooser.getSelectedFile();
        try{
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = ImageHelper.resize(icon.getImage(), 140, 160);
            ImageIcon resizeIcon = new ImageIcon(img);
            lbl_Image.setIcon(resizeIcon);
            personalImage = ImageHelper.toByteArray(img, "jpg");
        }catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
        
        
    }//GEN-LAST:event_btn_AnhActionPerformed

